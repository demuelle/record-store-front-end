import { useState } from 'react';
import ToDoAdder from './ToDoAdder';
import ToDo from './ToDo';

function ToDoList() {
  const [toDos, setToDos] = useState([]);

  const addToDo = toDo => {
    setToDos([...toDos, toDo]);
  };

  const remove = toDo => {
    setToDos(toDos.filter(x => x !== toDo));
  };

  return (
    <section>
      <ToDoAdder add={addToDo} />
      <ul>
        {toDos.map(toDo => (
          <ToDo toDo={toDo} deleteCallback={remove} />
        ))}
      </ul>
    </section>
  );
}

export default ToDoList;
