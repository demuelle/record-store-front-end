import { render, screen } from '@testing-library/react';
import { BrowserRouter as Router } from 'react-router-dom';
import Navigation from './Navigation';

test('renders Games link to "/"', () => {
  render(
    <Router>
      <Navigation />
    </Router>
  );
  expect(screen.getByText('Games').href).toBe(
    // Ports not included in links
    'http://localhost/'
  );
});

test('renders Consoles link to "/consoles"', () => {
  render(
    <Router>
      <Navigation />
    </Router>
  );
  expect(screen.getByText('Consoles').href).toBe('http://localhost/consoles');
});

