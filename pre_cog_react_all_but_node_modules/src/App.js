import TwoButtons from './TwoButtons';
import HappyPage from './HappyPage';
import SadPage from './SadPage';
import { useState } from 'react';
import Consoles from './Consoles.js';

function App() {
  const [feeling, setFeeling] = useState('');

  let page;
  if (feeling === 'Happy') {
    page = <HappyPage />;
  } else if (feeling === 'Sad') {
    page = <SadPage />;
  } else {
    page = <TwoButtons updateFeeling={setFeeling} />;
  }

  return (
    <>
      <h1>
        All The Feels.
        {feeling === '' ? null : (
          <button
            type="button"
            onClick={() => setFeeling('')}
            className="btn btn-outline-dark"
          >
            Change Mood
          </button>
        )}
        {page}
      </h1>
    </>
  );
}

export default App;
